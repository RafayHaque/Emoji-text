    emoji = request.form['emoji']
    emoji_lines = [''] * 5
    
    for char in word:
        if char in shapes_mapping:
            shape = shapes_mapping[char]
            # Replace the mapping emoji with the selected emoji
            shape = [line.replace('ğŸ”³', emoji) for line in shape]
            for i, line in enumerate(shape):
                emoji_lines[i] += line
    
